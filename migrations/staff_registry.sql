insert into public.staff (account_user_id, display_name, avatar_url)
select distinct up.user_id, coalesce(up."Username", 'Unnamed staff'), up.pfp_url
from public.user_profile up
join public.user_roles ur on ur.user_id = up.user_id
join public.roles r on r.id = ur.role_id
where r.name = 'staff'
on conflict (account_user_id) do update
set display_name = excluded.display_name,
    avatar_url = excluded.avatar_url,
    updated_at = now();

insert into public.staff (account_user_id, display_name, avatar_url)
select distinct up.user_id, coalesce(up."Username", 'Unnamed staff'), up.pfp_url
from public.mentor_groups mg
cross join lateral jsonb_array_elements_text(coalesce(mg.user_in_group, '[]'::jsonb)) as member(user_id)
join public.user_profile up on up.user_id = member.user_id::uuid
on conflict (account_user_id) do update
set display_name = excluded.display_name,
    avatar_url = excluded.avatar_url,
    updated_at = now();

insert into public.staff_traits (id, name, slug, description, metadata, is_active)
select mg.id,
       mg.group_name,
       lower(regexp_replace(mg.group_name, '[^a-zA-Z0-9]+', '-', 'g')),
       mg.description,
       '{}'::jsonb,
       true
from public.mentor_groups mg
on conflict (id) do update
set name = excluded.name,
    slug = excluded.slug,
    description = excluded.description;

select setval(
  pg_get_serial_sequence('public.staff_traits', 'id'),
  coalesce((select max(id) from public.staff_traits), 1),
  true
);

insert into public.staff_trait_assignments (staff_id, trait_id)
select distinct s.id, mg.id
from public.mentor_groups mg
cross join lateral jsonb_array_elements_text(coalesce(mg.user_in_group, '[]'::jsonb)) as member(user_id)
join public.staff s on s.account_user_id = member.user_id::uuid
on conflict (staff_id, trait_id) do nothing;