create table if not exists public.staff (
  id uuid primary key default gen_random_uuid(),
  account_user_id uuid null,
  display_name text not null,
  email text null,
  phone text null,
  avatar_url text null,
  job_title text null,
  status text not null default 'active',
  notes text null,
  profile jsonb not null default '{}'::jsonb,
  created_by uuid null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint staff_account_user_id_key unique (account_user_id),
  constraint staff_display_name_check check (display_name <> ''::text),
  constraint staff_status_check check (status = any (array['active'::text, 'inactive'::text, 'archived'::text])),
  constraint staff_profile_is_object check (jsonb_typeof(profile) = 'object'),
  constraint staff_account_user_id_fkey foreign key (account_user_id) references public.user_profile (user_id) on delete set null,
  constraint staff_created_by_fkey foreign key (created_by) references public.user_profile (user_id) on delete set null
) tablespace pg_default;

create index if not exists idx_staff_display_name on public.staff using btree (display_name) tablespace pg_default;
create index if not exists idx_staff_status on public.staff using btree (status) tablespace pg_default;

create table if not exists public.staff_traits (
  id bigint generated by default as identity primary key,
  name text not null,
  slug text null,
  description text null,
  metadata jsonb not null default '{}'::jsonb,
  category text null,
  is_active boolean not null default true,
  created_by uuid null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint staff_traits_name_key unique (name),
  constraint staff_traits_name_check check (name <> ''::text),
  constraint staff_traits_metadata_is_object check (jsonb_typeof(metadata) = 'object'),
  constraint staff_traits_created_by_fkey foreign key (created_by) references public.user_profile (user_id) on delete set null
) tablespace pg_default;

create index if not exists idx_staff_traits_slug on public.staff_traits using btree (slug) tablespace pg_default;
create index if not exists idx_staff_traits_category on public.staff_traits using btree (category) tablespace pg_default;

create table if not exists public.staff_trait_assignments (
  staff_id uuid not null,
  trait_id bigint not null,
  metadata jsonb not null default '{}'::jsonb,
  created_by uuid null,
  created_at timestamptz not null default now(),
  constraint staff_trait_assignments_pkey primary key (staff_id, trait_id),
  constraint staff_trait_assignments_metadata_is_object check (jsonb_typeof(metadata) = 'object'),
  constraint staff_trait_assignments_staff_id_fkey foreign key (staff_id) references public.staff (id) on delete cascade,
  constraint staff_trait_assignments_trait_id_fkey foreign key (trait_id) references public.staff_traits (id) on delete cascade,
  constraint staff_trait_assignments_created_by_fkey foreign key (created_by) references public.user_profile (user_id) on delete set null
) tablespace pg_default;

create index if not exists idx_staff_trait_assignments_trait_id on public.staff_trait_assignments using btree (trait_id) tablespace pg_default;

